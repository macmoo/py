# -------------------------------------------------
w   = 30
out = ""
print("------------------------------------------")
# MINE
for i in range(int(w/2)):
    for t in range(int(w/2)-i):
        out += " "
    for j in range(i*2+1):
        out += "*"
    out += "\n"
print(out)
print("------------------------------------------")
# BOOK
out2 = ""
for i in range(1,15):
    for j in range(14,i,-1):
        out2 += " "
    for k in range(0,i*2-1):
        out2 += "*"
    out2 += "\n"
print(out2)
print("------------------------------------------")
out3 = ""
for i in range(1,10):
    out3 += ("*" * i)
    out3 += "\n"
print(out3)
print("------------------------------------------")
